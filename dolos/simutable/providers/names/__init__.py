        if toss <= mf_prob:
            fnd = self.male_firstname  # Male first name dict
        else:
            fnd = self.female_firstname  # Female first name dict
        lnd = self.last_names  # Last name dict
        names = []
        for name_dict in [fnd, lnd]:
            names.append(self.generate_key(name_dict))
        fname = self._make_lower(list(names[0].lower()))
        lname = self._make_lower(list(names[1].lower()))
        return " ".join([fname, lname])
