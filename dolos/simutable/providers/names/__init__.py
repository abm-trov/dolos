    def generate_name(self, key, name_dict):
        """
        Method accepts a name dict
        as well as a key to extract the
        name from the dict.
        """

        # Thanks Stack Overflow
        return name_dict.get(key, name_dict[min(name_dict.keys(), key=lambda k: abs(k-key))])

    def generate_key(self, name_dict, a=0, b=1):
        """
        Method takes in dict and bounds
        and will return out the key value
        from the dict.
        """
        key = float(str(numpy.uniform(a, b))[:6])  # Generate a key
        return self.generate_name(key, name_dict)  # Get the entry

